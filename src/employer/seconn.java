    protected void login() {

        String pa;
        try {

            String url = "jdbc:mysql://localhost:3306/stock";
            String pwd = "";
            String user = "root";
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(url, user, pwd);

            if ("".equals(txtPass.getText())) {
                JOptionPane.showMessageDialog(new JFrame(), "Veuillez saisir votre mot de passe", "Erreur", JOptionPane.ERROR_MESSAGE);

            } else {

                int m = Integer.parseInt(txtMatricule.getText());
                pa = txtPass.getText();



                    String sql = "SELECT pass,services from employe where matricule=? ";
                    PreparedStatement pstmt = con.prepareStatement(sql);
                    pstmt.setInt(1, m);
                    ResultSet rs = pstmt.executeQuery();

                    if (rs.next()) {

                        String pass = rs.getString("pass"); // right password to the given login data
                        String services = rs.getString("services");

                        if (!pa.equals(pass)) {

                            JOptionPane.showMessageDialog(new JFrame(), "Votre mot de passe est incorrect.", "Erreur", JOptionPane.ERROR_MESSAGE);

                            return;

                        } else {
                            if (services.equals("gestionnaire de stock")) {
                                menu art = new menu(m);
                                art.setVisible(true);
                                this.dispose();

                            } else {

                                commande f = new commande(m);
                                f.setVisible(true);
                                this.dispose();

                            }

                        }

                    } else if (!rs.next()) {
                        JOptionPane.showMessageDialog(new JFrame(), "Cet employ√© n'existe pas.", "Erreur", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
//                } catch (SQLException e) {
//                    e.getMessage();
//                }
            }

        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(seconn.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

